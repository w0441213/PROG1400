package DataTypes;

public class DataTypes {

    public static void main(String[] args) {

        int myNum = 123456;
        short mySmallNum = 15000;
        byte myVerySmallNum = -100;
        float myFloat = 3.14159f;
        double myDouble = 2.31456;

        System.out.println("myNum = " +myNum);
        System.out.println("mySmallNum = " +mySmallNum);
        System.out.println("myVerySmallNum = " +myVerySmallNum);
        System.out.println("myFloat = " +myFloat);
        System.out.println("myDouble = " +myDouble);
    }

}
